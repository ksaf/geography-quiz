package com.orestis.velen.quiz.helpPowers;

public class NumberAndSign {
    private int number;
    private String sign;

    public NumberAndSign(int number, String sign) {
        this.number = number;
        this.sign = sign;
    }

    public int getNumber() {
        return number;
    }

    public String getSign() {
        return sign;
    }
}
